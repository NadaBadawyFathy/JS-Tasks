let typ1 = document.querySelector('.classes-to-add');
let typ2 = document.querySelector('.classes-to-remove');

let claa = document.querySelector('.classes-list div');
claa.innerHTML = 'No Classes To Show';

let c = 0;
typ1.onblur = function(){
    if (typ1.value.split(' ').length === 1 && typ1.value.length > 0){
        if (claa.innerHTML === 'No Classes To Show'){
            claa.innerHTML = '';
        }
        let span1 = document.createElement('span');
        span1.innerHTML = typ1.value.toLowerCase();
        span1.setAttribute('class',typ1.value.toLowerCase());
        let div = document.querySelector('.classes-list div');
        div.appendChild(span1);
        c++;
    }
    if (typ1.value.split(' ').length === 2){
        if (claa.innerHTML === 'No Classes To Show'){
            claa.innerHTML = '';
        }
        let span2 = document.createElement('span');
        let span3 = document.createElement('span');
        span2.innerHTML = typ1.value.split(' ')[0].toLowerCase();
        span2.setAttribute('class',typ1.value.split(' ')[0].toLowerCase());
        span3.innerHTML = typ1.value.split(' ')[1].toLowerCase();
        span3.setAttribute('class',typ1.value.split(' ')[1].toLowerCase());
        let div = document.querySelector('.classes-list div');
        div.appendChild(span2);
        div.appendChild(span3);
        c+=2;
    }
    typ1.value = '';
}

typ2.onblur = function(){
    if (typ2.value.split(' ').length === 1){
        let rem1 = document.querySelector(`.${typ2.value.toLowerCase()}`);
        rem1.remove();
        c--;
    }
    if (typ2.value.split(' ').length === 2){
        let rem2 = document.querySelector(`.${typ2.value.split(' ')[0].toLowerCase()}`);
        rem2.remove();
        let rem3 = document.querySelector(`.${typ2.value.split(' ')[1].toLowerCase()}`);
        rem3.remove();
        c-=2;
    }
    if (c < 1){
        claa.innerHTML = 'No Classes To Show';
    }
    typ2.value = '';
}
